package com.filipeduraes.workshop.core.maintenance;

/**
 * Define os tipos de elevadores disponíveis na oficina.
 * Cada tipo representa uma funcionalidade específica para manutenção de veículos.
 *
 * @author Filipe Durães
 */
public enum ElevatorType
{
    /**
     * Elevador de uso geral para manutenções básicas.
     */
    GENERAL_USE,
    
    /**
     * Elevador especializado para alinhamento e balanceamento de rodas.
     */
    ALIGNMENT_AND_BALANCING
}
